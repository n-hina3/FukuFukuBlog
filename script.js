/**
 * 全ページ共通：検索バー制御 & ARG入り口
 * index.html などどのページでも動作します
 */
function setupGlobalSearch() {
  const header = document.querySelector(".site-header");
  const wrapper = document.querySelector(".search-wrapper");
  const input = document.getElementById("arg-search");
  const icon = wrapper ? wrapper.querySelector("i") : null;

  if (!wrapper || !input || !icon) return;

  // 1. アイコンクリック時の開閉（トグル）
  icon.addEventListener("click", (e) => {
    e.stopPropagation();
    const isActive = wrapper.classList.contains("active");

    if (isActive) {
      wrapper.classList.remove("active");
      if (header) header.classList.remove("search-active"); // ナビゲーション再表示
      input.blur();
    } else {
      wrapper.classList.add("active");
      if (header) header.classList.add("search-active"); // ナビゲーション隠蔽
      input.focus();
    }
  });

  // 2. 検索バー内部クリック時は閉じない
  input.addEventListener("click", (e) => {
    e.stopPropagation();
  });

  // 3. 外側クリックで閉じる
  document.addEventListener("click", () => {
    if (wrapper.classList.contains("active")) {
      wrapper.classList.remove("active");
      if (header) header.classList.remove("search-active");
    }
  });

  // 4. ARGキーワード判定（Enterキー）
  input.addEventListener("keydown", (e) => {
    if (e.key === "Enter") {
      const val = input.value.trim();

      // ARGの正解キーワード
      const secretKeywords = [
        "再生",
        "さいせい",
        "サイセイ",
        "saisei",
        "Play",
        "PLAY",
        "play",
        "PLAY",
        "プレイ",
        "ぷれい",
      ];

      if (secretKeywords.some((word) => val.includes(word))) {
        // 演出：グリッチ後に quest.html へ遷移
        document.body.classList.add("glitching");
        input.disabled = true;
        setTimeout(() => {
          window.location.href = "quest.html";
        }, 1000);
      }
      // ヒント機能：米五郎左
      else if (val === "米五郎左") {
        alert(
          "【検索結果】米五郎左（こめごろうざ）\n\n戦国武将・丹羽長秀の通称。\n「米のように日々の生活に欠かせない存在」として、主君・織田信長から重用された。"
        );
        input.value = "";
      } else {
        // 通常の検索（見つからない）
        alert("該当する記事は見つかりませんでした。");
        input.value = "";
      }
    }
  });
}

// ハンバーガーメニュー
function initHamburgerMenu() {
  const hamburgerBtn = document.getElementById("hamburger-btn");
  const siteNav = document.getElementById("site-nav");
  const searchWrapper = document.querySelector(".search-wrapper");
  const headerActions = document.querySelector(".header-actions");

  if (!hamburgerBtn || !siteNav) return;

  hamburgerBtn.addEventListener("click", () => {
    const isActive = hamburgerBtn.classList.toggle("active");
    siteNav.classList.toggle("active");

    // モバイル時は検索窓をメニュー内に移動
    if (isActive && searchWrapper && siteNav) {
      siteNav.prepend(searchWrapper);
    } else if (!isActive && searchWrapper && headerActions) {
      headerActions.prepend(searchWrapper);
      searchWrapper.classList.remove("active");
    }
    document.body.style.overflow = isActive ? "hidden" : "";
  });

  const navLinks = siteNav.querySelectorAll("a");
  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      hamburgerBtn.classList.remove("active");
      siteNav.classList.remove("active");
      document.body.style.overflow = "";
      if (searchWrapper && headerActions) {
        headerActions.prepend(searchWrapper);
        searchWrapper.classList.remove("active");
      }
    });
  });
}

